package training2;

public class Calculator {

    public double sum(double a, double b) {
        return a + b;
    }

    public long sum(long a, long b) {
        return a + b;
    }

    public int sum(int a, int b) {
        return a + b;
    }

    public double division(double devident, double devisor) {
        return devident / devisor;
    }

    public double division(int devident, int devisor) {
        return (double) devident / devisor;
    }

    public double division(long devident, long devisor) {
        return (double) devident / devisor;
    }

    public double multiply(double a, double b) {
        return a * b;
    }

    public double multiply(int a, int b) {
        return a * b;
    }

    public double multiply(long a, long b) {
        return a * b;
    }

    public double subtraction(double reduce, double subtracted) {
        return reduce - subtracted;
    }

    public double subtraction(int reduce, int subtracted) {
        return reduce - subtracted;
    }

    public double subtraction(long reduce, long subtracted) {
        return reduce - subtracted;
    }

}
